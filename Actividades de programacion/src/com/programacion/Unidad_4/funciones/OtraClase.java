package com.programacion.Unidad_4.funciones;

public class OtraClase {

    /**
     * Metodo para sumar dos variables
     * @param a - sumando 1
     * @param b - sumando 2
     * @return resultado de la suma
     */

    public static int sumar (int a, int b){
        return a + b;
    }

    /**
     * Metodo para realizar resta
     * @param a numero 1
     * @param b numero 2
     * @return resultado de la resta
     */

    private static int restar(int a, int b) {
        return a - b;
    }

    /**
     * Imprimi por pantalla las operaciones
     */

    protected static void imprimisOperaciones(){
        System.out.println("Esra clase ");
    }




}
